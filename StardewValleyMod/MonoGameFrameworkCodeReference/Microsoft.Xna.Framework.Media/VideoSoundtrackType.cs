namespace Microsoft.Xna.Framework.Media;

/// <summary>
/// Type of sounds in a video
/// </summary>
public enum VideoSoundtrackType
{
	/// <summary>
	/// This video contains only music.
	/// </summary>
	Music,
	/// <summary>
	/// This video contains only dialog.
	/// </summary>
	Dialog,
	/// <summary>
	/// This video contains music and dialog.
	/// </summary>
	MusicAndDialog
}
